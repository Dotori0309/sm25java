package control;

public class Control1 {
    public static void main(String[] args) {
        int grade = 1;
        switch (grade) {
            case 1: System.out.println("냉");
            case 2: System.out.println("세");
            case 3: System.out.println("티");
                break;
            default: System.out.println("없");
        }
    }
}
